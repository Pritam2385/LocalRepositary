package Assignment1;

public class Example17 {
	public static void main(String args[]) {
	System.out.println("***********Printing 0 to 10 number**********");
	for (int i = 0; i < 11; i++) {
		System.out.println(i);
	}
	System.out.println("*************Reverse 10 to 1****************");
	for (int i = 10; i > 0; i--) {
		System.out.println(i);
	}
}
}